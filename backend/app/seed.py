books = [
    Book(
        title=title,
        author=author,
        published_date=published_date,
        isbn=isbn,
        cover_url=cover_urls_by_isbn[isbn],
        download_url=download_url,
    )
    for title, author, published_date, isbn, download_url in book_seed_data
]


async def seed_books():
    async with async_session() as session:
        result = await session.execute(select(Book))
        existing_books = {book.isbn: book for book in result.scalars().all()}
        new_books = [book for book in books if book.isbn not in existing_books]
        updated_books = []

        for book in books:
            existing_book = existing_books.get(book.isbn)
            if not existing_book:
                continue

            should_update = False

            if existing_book.cover_url != book.cover_url:
                existing_book.cover_url = book.cover_url
                should_update = True

            if existing_book.download_url != book.download_url:
                existing_book.download_url = book.download_url
                should_update = True

            if should_update:
                updated_books.append(existing_book)

        if not new_books and not updated_books:
            return

        session.add_all([*new_books, *updated_books])

        await session.commit()
